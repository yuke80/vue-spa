              日本では、2004年5月にスタジオ・ヨギーが1号店を開始します。ヨギーは同年9月にはヨガインストラクター養成講座を開設するなどし、日本のヨガマーケット拡大を推し進めた企業の1つであると言えます。さらに、ヨギー1号店と同年2004年の8月に、LAVAがホットヨガスタジオを開始しました。オープンから約2年で50店舗を展開し、ホットヨガブームを牽引しました。2005年にワークアウトワールド・ジャパンが、米ビクラムとライセンス契約を締結し、2006年にビクラムヨガの1号店を銀座に出店します(2008年自己破産)。米国ではハリウッドが発祥だったため、ハリウッド俳優がやるヨガという訴求で注目を集め、広まりました。ビクラムヨガ銀座1号店は内装も豪華で、オープン時は、1日100人の体験者が集まるほど人気がありました。2011年には、スポーツジムを運営するINSPAがホットヨガ専門スタジオ・カルドを開始し、2010年頃から大手スポーツジム(セントラル、ルネサンス、NASなど)が、既存スタジオを改修してホットヨガができる環境を作るなど、ホットヨガ市場への参入が相次ぎ、LAVA、ヨギーが何スタジオか閉店する等、市場が成熟期に入りました。そんなヨガ市場が成長~成熟する中、2006年に私は、ピラティス1号店を中目黒に誕生させました。
